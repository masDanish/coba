void main() {
  var first = 35;
  var late = 3;

  //penjumlahan
  print(first + late);
  //pengurangan
  print(first - late);
  //perkalian
  print(first * late);
  //pembagian, hasil double
  print(first / late);
  //pembagian, hasil int
  print(first ~/ late);
  //sisa bagi
  print(first % late);

  
}
