///kata kunci null merupakan tipe data representasi data kosong
///secara default, saat kita membuat variable,data haru diisi, jika tidak diisi, maka variable tidak bisa digunakan
///namun jika kita ingin membuat sebuah variable bisa diisi data null, kita bisa tambahkan karakter '?' (tanda tanya), seperti TipeData namaVariable = null;

void main() {
  String nama1 = 'bizzare';
  String? nama2;

  print(nama1); 
  print(nama2); // null
}
